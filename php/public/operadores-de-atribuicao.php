<?php 
    // O operador de atribuição é o '='(igual);
    $name = "Gabriel";

    // O ponto (.) é usado para concatenar
    $name = $name . " Correa";

    // concatenar (+=)
    $name .= " Correa";

    $number = 10;

    //concatenar / somar numeros
    $number += 20;
    echo $number;
    echo "<br>";
    //concatenar / subtrair numeros
    $number -= 20;
    echo $number;
    echo "<br>";
    
    // concatenar / dividir numeros
    $number /= 20;
    echo $number;
    echo "<br>";
    
    // concatenar / multiplicar numeros
    $number *= 20;
    echo $number;
    echo "<br>";
    
    // concatenar / resto da divisao  numeros
    $number %= 20;
    echo $number;
    echo "<br>";

?>